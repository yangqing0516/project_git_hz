function commonCode() {
    $.each(dataList, function (i, items) {
        $.each(items, function (j, item) {
            $.each(item.modelInfo, function (l, list) {
                list.parentId = item.id;
                list.id = l;
                list.strartTime = formatDate(new Date(list.strartTime));
                list.endTime = formatDate(new Date(list.endTime));
                /* status值 3：已结束 2：已开始 1：待开始 */
                if (nowTime > list.strartTime && nowTime < list.endTime) {
                    list.status = 2;
                } else if (nowTime < list.strartTime) {
                    list.status = 1;
                } else if (nowTime > list.endTime) {
                    list.status = 3;
                }
            })
        })
    })
}
